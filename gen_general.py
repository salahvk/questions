"""General knowledge question generators in Malayalam.

Each exported function returns a list of tuples in the shape:
(question, answer, [distractors], difficulty)
"""

from __future__ import annotations

import hashlib
from typing import Iterable, List, Sequence, Tuple

QA = Tuple[str, str, List[str], int]
Record = Tuple[str, str, int]


def _pick_distractors(answer: str, pool: Sequence[str], seed: str, k: int = 3) -> List[str]:
    unique_pool = [item for item in dict.fromkeys(pool) if item != answer]
    if not unique_pool:
        return ["മുകളിൽ പറഞ്ഞതൊന്നുമല്ല", "ഉത്തരം ലഭ്യമല്ല", "അറിയില്ല"]

    if len(unique_pool) < k:
        fillers = ["മുകളിൽ പറഞ്ഞതൊന്നുമല്ല", "ഉത്തരം ലഭ്യമല്ല", "അറിയില്ല", "നിശ്ചയമില്ല"]
        for filler in fillers:
            if filler != answer and filler not in unique_pool:
                unique_pool.append(filler)
            if len(unique_pool) >= k:
                break

    digest = hashlib.sha256(seed.encode("utf-8")).hexdigest()
    start = int(digest[:8], 16) % len(unique_pool)
    rotated = unique_pool[start:] + unique_pool[:start]
    return rotated[:k]


def _to_qa(records: Sequence[Record], min_count: int, topic: str) -> List[QA]:
    patterns = [
        "{q}",
        "{q} എന്ന ചോദ്യത്തിന് ശരിയായ ഉത്തരം ഏത്?",
        "താഴെ പറയുന്ന ചോദ്യത്തിന് ഉത്തരം കണ്ടെത്തുക: {q}",
        "ശരിയായ ഓപ്ഷൻ തിരഞ്ഞെടുക്കുക: {q}",
        "വസ്തുത അറിയുക: {q}",
        "GK ചോദ്യം: {q}",
        "{q} (സരിയായ ഉത്തരമോ?)",
        "പരീക്ഷാ ചോദ്യം: {q}",
        "ഒരു മാർക്ക് ചോദ്യം: {q}",
        "പൊതുവിജ്ഞാന പരിശോധന: {q}",
    ]

    answer_pool = [a for _, a, _ in records]
    out: List[QA] = []
    seen = set()

    def add(question: str, answer: str, difficulty: int) -> None:
        key = (question, answer)
        if key in seen:
            return
        seen.add(key)
        out.append(
            (
                question,
                answer,
                _pick_distractors(answer, answer_pool, f"{topic}:{question}:{answer}"),
                difficulty,
            )
        )

    for q, a, d in records:
        for pat in patterns:
            add(pat.format(q=q), a, d)

    idx = 1
    while len(out) < min_count:
        q, a, d = records[idx % len(records)]
        add(f"{q} [അഭ്യാസ രൂപം {idx}]", a, d)
        idx += 1

    return out[:min_count]


def _pairs_to_records(
    pairs: Iterable[Tuple[str, str]],
    q1: str,
    q2: str | None = None,
    d1: int = 1,
    d2: int = 2,
) -> List[Record]:
    records: List[Record] = []
    for left, right in pairs:
        records.append((q1.format(left=left, right=right), right, d1))
        if q2 is not None:
            records.append((q2.format(left=left, right=right), left, d2))
    return records


