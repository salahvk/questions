def main() -> None:
    path = BASE / FILE
    data = json.loads(path.read_text(encoding="utf-8"))
    questions = data.setdefault("questions", [])
    global_stems = load_global_stems(path)
    existing = {q["question"].strip() for q in questions}
    n = max_id_num(questions, PREFIX)

    candidates: list[dict] = []
    skipped = 0
    for q, opts, ans, diff in rows():
        if q in existing or q in global_stems:
            skipped += 1
            continue
        if len(set(opts)) != 4 or ans not in opts:
            print(f"SKIP invalid: {q[:60]}", file=sys.stderr)
            skipped += 1
            continue
        if MIXED.search(q + "".join(opts) + ans):
            print(f"SKIP mixed: {q[:60]}", file=sys.stderr)
            skipped += 1
            continue
        candidates.append({"question": q, "options": opts, "answer": ans, "difficulty": diff})

    spread = spread_consecutive_templates(candidates, random.Random(42), max_run=2)
    added = 0
    for item in spread:
        n += 1
        entry = {
            "id": f"{PREFIX}{n:04d}",
            "question": item["question"],
            "options": item["options"],
            "answer": item["answer"],
            "difficulty": item["difficulty"],
        }
        questions.append(entry)
        existing.add(item["question"])
        global_stems.add(item["question"])
        added += 1

    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"{FILE}: +{added} (skipped {skipped})")
    subprocess.run([sys.executable, "validate_questions.py", FILE], cwd=BASE, check=False)


if __name__ == "__main__":
    main()
