def _pick3(pool: list[str], correct: str, rng: random.Random) -> list[str]:
    wrong = [x for x in pool if x != correct]
    rng.shuffle(wrong)
    opts = [correct] + wrong[:3]
    while len(opts) < 4:
        opts.append("ഒന്നുമില്ല")
    return opts[:4]


