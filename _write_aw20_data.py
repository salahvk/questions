def no_latin(rows: list, label: str, idx: int = 0) -> None:
    for row in rows:
        if LATIN.search(row[idx]):
            raise SystemExit(f"Latin in {label} name: {row[idx]!r}")


def dedupe(rows: list) -> list:
    seen: set = set()
    out: list = []
    for row in rows:
        if row not in seen:
            seen.add(row)
            out.append(row)
    return out


