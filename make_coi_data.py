def main() -> None:
    g = globals()
    lines = ["# coi20_data.py — verified Indian Constitution fact rows (wave 20)\n\n"]
    for name in NAMES:
        if name not in g:
            raise SystemExit(f"missing {name}")
        lines.append(f"{name}: list = ")
        lines.append(pprint.pformat(g[name], width=120, sort_dicts=False))
        lines.append("\n\n")
    OUT.write_text("".join(lines), encoding="utf-8")
    print(f"Wrote {OUT}")
    for name in NAMES:
        print(f"  {name}: {len(g[name])}")

if __name__ == "__main__":
    main()
