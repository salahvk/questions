def _check(rows: list, label: str) -> list:
    for row in rows:
        for cell in row:
            if MIXED.search(cell):
                raise ValueError(f"{label}: mixed script in {cell!r}")
            if re.search(r"[a-zA-Z]{3,}", cell):
                raise ValueError(f"{label}: Latin leak in {cell!r}")
    return rows


