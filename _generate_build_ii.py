def validate(d: dict[str, list[tuple[str, str]]]) -> list[str]:
    bad = []
    for cat, rows in d.items():
        for a, b in rows:
            if MIXED.search(a + b):
                bad.append(f"{cat}: ({a!r}, {b!r})")
    return bad


