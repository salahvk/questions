def dedupe(rows: list[tuple]) -> list[tuple]:
    return list(dict.fromkeys(rows))


def _fix_name(name: str) -> str:
    fixes = {"അൻil കുംബ്ലെ": "അനിൽ കുംബ്ലെ"}
    return fixes.get(name, name)


def no_latin(rows: list, label: str, idx: int = 0) -> None:
    for row in rows:
        if LATIN.search(row[idx]):
            raise SystemExit(f"Latin in {label} person name: {row[idx]!r}")


def _header() -> str:
    raw = OUT.read_text(encoding="utf-8")
    key = "GALLANTRY_HIERARCHY = ["
    pos = raw.find(key)
    if pos < 0:
        raise SystemExit("GALLANTRY_HIERARCHY missing in awards_wave20_data.py")
    start = raw.index("[", pos)
    depth = 0
    end = start
    for j in range(start, len(raw)):
        if raw[j] == "[":
            depth += 1
        elif raw[j] == "]":
            depth -= 1
            if depth == 0:
                end = j + 1
                break
    header = raw[:end] + "\n\n"
    if "NATIONAL_FILM_BEST_ACTOR" not in header:
        header = header.replace(
            "from __future__ import annotations\n\n",
            "from __future__ import annotations\n\n"
            "from awards_facts import NATIONAL_FILM_BEST_ACTOR\n\n",
        )
    # Drop duplicate build_all_data from header; rewritten at file end.
    if "def build_all_data()" in header:
        header = header[: header.index("def build_all_data()")]
    return header


