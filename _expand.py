def dedupe(rows: list[tuple], key: int = 0) -> list[tuple]:
    seen: set[str] = set()
    out: list[tuple] = []
    for row in rows:
        k = row[key]
        if k in seen:
            continue
        seen.add(k)
        out.append(row)
    return out


def emit(name: str, rows: list[tuple], comment: str = "") -> str:
    lines: list[str] = []
    if comment:
        lines.append(f"# {comment}")
    lines.append(f"{name}: list[tuple[str, ...]] = [")
    for row in rows:
        parts = ", ".join(f'"{c}"' for c in row)
        lines.append(f"    ({parts}),")
    lines.append("]")
    return "\n".join(lines)


