from __future__ import annotations

from collections import defaultdict
from typing import Callable, Dict, List, Sequence, Tuple

QuestionTuple = Tuple[str, str, List[str], str]


def _difficulty_for_template(template_index: int, total_templates: int) -> str:
    if template_index < total_templates // 3:
        return "easy"
    if template_index < (2 * total_templates) // 3:
        return "medium"
    return "hard"


def _pick_distractors(answer: str, pool: Sequence[str], salt: int) -> List[str]:
    options = [item for item in pool if item != answer]
    if len(options) < 3:
        fallback = [item for item in _ALL_FALLBACK_OPTIONS if item != answer]
        for item in fallback:
            if item not in options:
                options.append(item)
            if len(options) >= 3:
                break
    if not options:
        return ["കേരളം", "തിരുവനന്തപുരം", "മലയാളം"]

    start = salt % len(options)
    picked: List[str] = []
    for offset in range(len(options)):
        item = options[(start + offset) % len(options)]
        if item not in picked:
            picked.append(item)
        if len(picked) == 3:
            break
    while len(picked) < 3:
        picked.append(options[len(picked) % len(options)])
    return picked


def _expand_facts(
    facts: Sequence[Dict[str, str]],
    templates: Sequence[Callable[[Dict[str, str]], str]],
    min_count: int,
) -> List[QuestionTuple]:
    pools: Dict[str, List[str]] = defaultdict(list)
    for fact in facts:
        pools[fact["tag"]].append(fact["answer"])

    questions: List[QuestionTuple] = []
    seen_questions = set()
    salt = 0

    for t_index, template in enumerate(templates):
        difficulty = _difficulty_for_template(t_index, len(templates))
        for fact in facts:
            question = template(fact).strip()
            if question in seen_questions:
                continue
            seen_questions.add(question)
            answer = fact["answer"]
            distractors = _pick_distractors(answer, pools[fact["tag"]], salt)
            questions.append((question, answer, distractors, difficulty))
            salt += 7
            if len(questions) >= min_count:
                return questions

    if len(questions) < min_count:
        # Secondary expansion: use indexed variants for uniqueness when needed.
        variant = 1
        while len(questions) < min_count:
            for t_index, template in enumerate(templates):
                difficulty = _difficulty_for_template(t_index, len(templates))
                for fact in facts:
                    question = f"{template(fact).strip()} (വേരിയന്റ് {variant})"
                    if question in seen_questions:
                        continue
                    seen_questions.add(question)
                    answer = fact["answer"]
                    distractors = _pick_distractors(answer, pools[fact["tag"]], salt)
                    questions.append((question, answer, distractors, difficulty))
                    salt += 11
                    if len(questions) >= min_count:
                        return questions
            variant += 1

    return questions


