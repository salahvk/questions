def fmt_list(name: str, rows: list[tuple]) -> str:
    lines = [f"{name}: list[tuple[str, ...]] = ["]
    for row in rows:
        lines.append("    (" + ", ".join(repr(c) for c in row) + "),")
    lines.append("]")
    return "\n".join(lines)


