def _dedupe(rows: list[tuple[str, str]]) -> list[tuple[str, str]]:
    seen: set[str] = set()
    out: list[tuple[str, str]] = []
    for a, b in rows:
        if a in seen:
            continue
        seen.add(a)
        out.append((a, b))
    return out


