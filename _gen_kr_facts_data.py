MIXED = re.compile(r"[\u0D00-\u0D7F][a-zA-Z]|[a-zA-Z][\u0D00-\u0D7F]")
ALLOW = re.compile(r"^(PRDS|NSS|SNDP|പി\.ആർ\.ഡി\.എസ്\.|എൻ\.എസ്\.എസ്\.|എസ്\.എൻ\.ഡി\.പി\.)$")


def ok(s: str) -> bool:
    if ALLOW.match(s.strip()):
        return True
    if MIXED.search(s):
        return False
    return not re.search(r"[a-zA-Z]{3,}", s)


def clean(rows: list[tuple[str, str]]) -> list[tuple[str, str]]:
    seen: set[tuple[str, str]] = set()
    out: list[tuple[str, str]] = []
    for a, b in rows:
        if not (ok(a) and ok(b)) or (a, b) in seen:
            continue
        seen.add((a, b))
        out.append((a, b))
    return out


def ct(rows: list[tuple[str, str, str]]) -> list[tuple[str, str, str]]:
    seen: set[tuple[str, str, str]] = set()
    out: list[tuple[str, str, str]] = []
    for a, b, c in rows:
        if not all(ok(x) for x in (a, b, c)) or (a, b, c) in seen:
            continue
        seen.add((a, b, c))
        out.append((a, b, c))
    return out


