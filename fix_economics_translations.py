def is_translation_style(q: dict) -> bool:
    opts = q.get("options", [])
    if len(opts) != 4:
        return False
    question = q.get("question", "")
    if not re.search(r"(എന്താണ്|എന്ത്|എന്തിന്|എന്തെന്നാൽ)", question):
        return False
    generic_count = sum(1 for o in opts if o in GENERIC)
    if generic_count >= 2:
        return True
    answer = q.get("answer", "")
    if generic_count >= 1 and len(answer) < 30:
        non_generic = [o for o in opts if o not in GENERIC]
        if len(non_generic) == 1 and answer == non_generic[0]:
            return True
    return False


def extract_term(question: str) -> str:
    m = re.match(r"^എന്താണ് (.+?)\??$", question.strip())
    if m:
        return m.group(1).strip()
    m = re.match(r"^(.+?) എന്ത് സൂചിപ്പിക്കുന്നു\??$", question.strip())
    if m:
        return m.group(1).strip()
    return question.strip()


def pick_pool(question: str, answer: str, term: str) -> str:
    text = f"{question} {answer} {term}".lower()
    if any(k in text for k in ("നികുതി", "ടാക്സ്", "ഡ്യൂട്ടി", "വാറ്റ", "gst", "സർചാർജ്")):
        return "tax"
    if any(k in text for k in ("വിപണി", "മാർക്കറ്റ്", "ഓഹരി", "ബോണ്ട്", "ഡിവിഡൻഡ്")):
        return "market"
    if any(k in text for k in ("കയറ്റുമതി", "ഇറക്കുമതി", "ട്രേഡ്", "വ്യാപാര", "കസ്റ്റംസ്", "എക്സ്പോർട്ട്", "ഇംപോർട്ട്")):
        return "trade"
    if any(k in text for k in ("ബാങ്ക്", "വായ്പ", "ലോൺ", "അക്കൗണ്ട്", "എടിഎം", "കാർഡ്", "നിക്ഷേപ", "ഡെപ്പോസിറ്റ്")):
        return "banking"
    return "general"


def expand_correct(answer: str, term: str, pool: str) -> str:
    if len(answer) >= 28:
        return answer
    action_words = ("നൽകുന്ന", "ചെയ്യുന്ന", "സൂക്ഷിക്ക", "കൈമാറ", "ഈടാക്ക", "തിരിച്ച", "വാങ്ങ", "വിൽക്ക")
    if len(answer) >= 18 and any(w in answer for w in action_words):
        return answer
    if pool == "banking" and len(answer) >= 22:
        return answer
    if pool == "tax":
        return f"നികുതി സംവിധാനത്തിൽ {answer} നടപ്പിലാക്കുന്ന പ്രക്രിയ"
    if pool == "market":
        return f"വിപണിയിൽ {answer} നടപ്പിലാക്കുന്ന പ്രക്രിയ"
    if pool == "trade":
        return f"അന്താരാഷ്ട്ര വ്യാപാരത്തിൽ {answer} നടപ്പിലാക്കുന്ന പ്രക്രിയ"
    return f"സാമ്പത്തികമായി {answer} എന്നതിനെ സൂചിപ്പിക്കുന്ന പ്രവർത്തി"


