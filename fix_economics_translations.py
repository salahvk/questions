def expand_short_answers(questions: list) -> int:
    expanded = 0
    for q in questions:
        if len(q.get("answer", "")) >= 28:
            continue
        old_answer = q["answer"].strip()
        new_answer = smart_expand_answer(old_answer, q["question"])
        if new_answer == old_answer:
            continue
        opts = [new_answer if o == old_answer else o for o in q["options"]]
        opts = [o for o in opts if o != new_answer]
        opts.insert(0, new_answer)
        seen = set()
        unique = []
        for o in opts:
            if o not in seen:
                seen.add(o)
                unique.append(o)
        while len(unique) < 4:
            extra = pick_wrong(pick_pool(q["question"], old_answer, ""), seen, 1)
            if extra:
                unique.append(extra[0])
                seen.add(extra[0])
        random.shuffle(unique)
        q["options"] = unique[:4]
        q["answer"] = new_answer
        expanded += 1
    return expanded


def transform_question(q: dict) -> dict:
    question = q["question"].strip()
    answer = q["answer"].strip()

    if question in OVERRIDES:
        new_q, correct, wrongs = OVERRIDES[question]
        mcq = make_mcq(new_q, correct, wrongs, "hard")
        return {**q, **mcq, "difficulty": "hard"}

    term = extract_term(question)
    pool_name = pick_pool(question, answer, term)
    new_q = make_question(term, pool_name)
    correct = expand_correct(answer, term, pool_name)
    exclude = {correct, answer}
    wrongs = pick_wrong(pool_name, exclude, 3)

  # Also exclude non-generic old options if explanatory enough
    for opt in q["options"]:
        if opt not in GENERIC and len(opt) >= 20 and opt != answer:
            wrongs = [opt] + [w for w in wrongs if w != opt]
            wrongs = wrongs[:3]
            break

    while len(wrongs) < 3:
        wrongs.append(pick_wrong("general", exclude | set(wrongs), 1)[0])

    mcq = make_mcq(new_q, correct, wrongs, "hard")
    return {**q, **mcq, "difficulty": "hard"}


def main():
    path = "economics.json"
    with open(path, encoding="utf-8") as f:
        data = json.load(f)

    transformed = 0
    for q in data["questions"]:
        if is_translation_style(q):
            updated = transform_question(q)
            q.update(updated)
            transformed += 1

    expanded = expand_short_answers(data["questions"])

    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    # Validate
    bad_answers = [q["id"] for q in data["questions"] if q["answer"] not in q["options"]]
    remaining = sum(1 for q in data["questions"] if is_translation_style(q))
    print(f"Transformed: {transformed}")
    print(f"Expanded short answers: {expanded}")
    print(f"Remaining translation-style: {remaining}")
    if bad_answers:
        print(f"Answers not in options: {len(bad_answers)}")
    else:
        print("All answers valid.")


if __name__ == "__main__":
    main()
