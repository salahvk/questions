def pick_wrong(pool_name: str, exclude: set[str], n: int = 3) -> list[str]:
    pool = WRONG_POOLS.get(pool_name, WRONG_POOLS["general"])
    candidates = [w for w in pool if w not in exclude]
    random.shuffle(candidates)
    if len(candidates) < n:
        extra = [w for w in WRONG_POOLS["general"] if w not in exclude and w not in candidates]
        candidates.extend(extra)
    return candidates[:n]


