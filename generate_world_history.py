def main():
    seen, files = load_existing()
    questions = []
    counter = 0
    prefix = "wh"

    # Keep existing wh_001 if present
    existing = files.get("world_history.json", {}).get("questions", [])
    if existing:
        q0 = existing[0]
        questions.append(q0)
        seen.add(q0["question"].strip())
        counter = max(counter, int(q0["id"].split("_")[1]))

    for item in WORLD_HISTORY:
        if len(questions) >= 200:
            break
        qtext, answer, wrong, diff = item
        mcq = make_mcq(qtext, answer, wrong, diff)
        counter, added = add_unique(seen, questions, prefix, counter, mcq)
        if not added:
            continue

    if len(questions) < 200:
        raise SystemExit(f"Only generated {len(questions)} questions; need 200")

    out = {"questions": questions}
    with open("world_history.json", "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False, indent=2)
        f.write("\n")
    print(f"Wrote {len(questions)} questions to world_history.json")


if __name__ == "__main__":
    main()
