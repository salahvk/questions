def _extract_list(name: str, src: str) -> list:
    m = re.search(rf"^{name}: list = (\[.*?\])\n\n", src, re.M | re.S)
    if not m:
        raise ValueError(f"Missing {name} in partial file")
    return ast.literal_eval(m.group(1))


