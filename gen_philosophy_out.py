def write_output(d: dict[str, list[tuple[str, str]]], direct: list[tuple[str, str, list[str], str]]) -> int:
    lines = [
        '#!/usr/bin/env python3\n"""Wave 30 philosophy facts — 30 Malayalam PSC topic types."""\n\n'
        "from __future__ import annotations\n\nimport random\n\n"
        "from refill_common import Candidate\nfrom wave30_emit import emit_category, emit_direct\n\n"
    ]
    for var, _ in META:
        lines.append(f"{var}: list[tuple[str, str]] = {pprint.pformat(d[var], width=120, sort_dicts=False)}\n")
    lines.append(
        f"DIRECT_FACTS: list[tuple[str, str, list[str], str]] = "
        f"{pprint.pformat(direct, width=120, sort_dicts=False)}\n"
    )
    lines.append("\ndef generate_wave30_candidates(existing: set[str], rng: random.Random) -> list[Candidate]:\n    out: list[Candidate] = []\n")
    for var, comment in META:
        lines.append(f"    # {comment}\n")
        lines.append(f"    emit_category(out, existing, rng, {var},\n")
        lines.append(f"        {pprint.pformat(FWD, width=120)},\n")
        lines.append(f"        {pprint.pformat(REV, width=120)},\n")
        lines.append(f"        [a for a, _ in {var}], [b for _, b in {var}])\n")
    lines.append("    emit_direct(out, existing, rng, DIRECT_FACTS)\n    return out\n\n\n")
    lines.append('if __name__ == "__main__":\n    print(len(generate_wave30_candidates(set(), random.Random(0))))\n')
    OUT.write_text("".join(lines), encoding="utf-8")

    spec = importlib.util.spec_from_file_location("outmod", OUT)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return len(mod.generate_wave30_candidates(set(), random.Random(0)))


def main() -> None:
    data = load_base_data()
    add_tail(data)
    missing = [n for n, _ in META if n not in data]
    if missing:
        raise RuntimeError(f"missing categories: {missing}")
    count = write_output(data, build_direct())
    print(f"Wrote {OUT}")
    print(f"Count: {count}")
    if count < 800:
        sys.exit(1)


if __name__ == "__main__":
    main()
