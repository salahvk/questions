def is_bad_kept(question: str, options: list[str], answer: str) -> bool:
    """Drop legacy rows with English leaks or malformed stems."""
    if is_filler(question):
        return True
    if "നൽകുന്നത്/ബന്ധപ്പെട്ടത്" in question:
        return True
    if "Recording Academy" in options or answer == "Recording Academy":
        return True
    if "FIFAയുടെ" in answer or any("FIFAയുടെ" in o for o in options):
        return True
    if any("Sveriges Riksbank" in o for o in options) or "Sveriges Riksbank" in answer:
        return True
    if re.search(r"സൂചിപ്പിക്കുന്ന|Sveriges", question + answer):
        return True
    blob = question + " " + answer + " " + " ".join(options)
    if "Bharat Ratna scientists" in question or re.search(r"\bPritzker\b", blob):
        return True
    return False


def is_filler(question: str) -> bool:
    return any(p.search(question) for p in FILLER_PATTERNS)


def load_global_existing() -> set[str]:
    existing: set[str] = set()
    for path in BASE.glob("*.json"):
        if path.name == "awards.json":
            continue
        try:
            data = json.loads(path.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            continue
        for q in data.get("questions", []):
            text = q.get("question", "").strip()
            if text:
                existing.add(text)
    return existing


def git_head_questions() -> list[dict]:
    try:
        raw = subprocess.check_output(["git", "show", "HEAD:awards.json"], cwd=BASE)
        return json.loads(raw).get("questions", [])
    except (subprocess.CalledProcessError, json.JSONDecodeError):
        return []


def make_entry(num: int, q: str, opts: list[str], ans: str, diff: str, *, width: int = 4) -> dict:
    shuffled = list(opts)
    random.shuffle(shuffled)
    return {
        "id": f"{PREFIX}{num:0{width}d}",
        "question": q,
        "options": shuffled,
        "answer": ans,
        "difficulty": diff,
    }


def pick3(pool: list[str], correct: str) -> list[str]:
    choices = list(dict.fromkeys(x for x in pool if x != correct))
    random.shuffle(choices)
    fallbacks = [
        "പത്മശ്രീ", "പത്മഭൂഷൺ", "അർജുന അവാർഡ്", "നോബൽ സമ്മാനം", "ഓസ്കാർ അവാർഡ്",
        "1965", "1975", "1985", "1995", "2005", "2015", "2020",
        "സാഹിത്യം", "ചലച്ചിത്രം", "ക്രിക്കറ്റ്", "ഹോക്കി",
    ]
    while len(choices) < 3:
        for x in fallbacks:
            if x != correct and x not in choices:
                choices.append(x)
    return choices[:3]


def add_candidate(
    out: list,
    existing: set[str],
    q: str,
    opts: list[str],
    ans: str,
    diff: str,
) -> None:
    q = q.strip()
    if not q or q in existing or is_filler(q):
        return
    opts = list(dict.fromkeys(opts))
    if ans not in opts:
        opts = [ans] + [o for o in opts if o != ans]
    if len(opts) < 4:
        opts = opts + pick3(opts, ans)
    opts = opts[:4]
    if len(set(opts)) != 4:
        return
    out.append((q, opts, ans, diff))
    existing.add(q)


