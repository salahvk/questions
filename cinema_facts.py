def _emit_legacy(out: list[Candidate], existing: set[str], rng: random.Random) -> None:
    for q, ans, wrong, diff in LEGACY_FACTS:
        add_candidate(out, existing, rng, q, ans, wrong, diff)


def generate_candidates(existing: set[str], rng: random.Random) -> list[Candidate]:
    wave = generate_wave20_candidates(existing, rng)
    legacy: list[Candidate] = []
    _emit_legacy(legacy, existing, rng)
    return interleave_candidates(wave + legacy, rng)
