import ast as _ast
import re as _re

_facts_src = (Path(__file__).parent / "kerala_renaissance_facts.py").read_text(encoding="utf-8")
_legacy_facts = _ast.literal_eval(
    _facts_src.split("FACTS: list[tuple[str, str, list[str], str]] = ")[1].split("\n\n")[0]
)

def _fix_legacy(q: str, ans: str) -> str:
    q = q.replace("SNDP", "എസ്.എൻ.ഡി.പി.").replace("PRDS", "പി.ആർ.ഡി.എസ്.")
    if _re.match(r"^\d{4}$", ans.strip()):
        q = q.replace(" ആരാണ്/എതാണ്?", " ഏത്?").replace(" ആരാണ്/എതാണ്", " ഏത്")
    return q.strip()

def _variants(q: str, ans: str, wrong: list[str], diff: str) -> list[tuple[str, str, list[str], str]]:
    rows = []
    base = _fix_legacy(q, ans)
    if not base or ans == "ഓപ്ഷൻ 3" or any("ഓപ്ഷൻ" in w for w in wrong):
        return rows
    wrong = [w.replace("PRDS", "പി.ആർ.ഡി.എസ്.").replace("SNDP", "എസ്.എൻ.ഡി.പി.") for w in wrong if w != ans and w != "ഓപ്ഷൻ 3"]
    if len({ans, *wrong}) < 4:
        return rows
    rows.append((base, ans, wrong[:3], diff))
    if base.endswith("?"):
        alt = base
        if " ആരാണ്?" in alt:
            alt2 = alt.replace(" ആരാണ്?", " ഏത്?")
            if alt2 != alt:
                rows.append((alt2, ans, wrong[:3], diff))
        if " ഏത്?" in alt and "വർഷം" in alt:
            alt3 = alt.replace(" ഏത്?", " എപ്പോൾ?")
            rows.append((alt3, ans, wrong[:3], diff))
        if not alt.startswith("കേരള നവോത്ഥാന"):
            rows.append((f"കേരള നവോത്ഥാനത്തിൽ — {alt}", ans, wrong[:3], diff))
    return rows

for q, ans, wrong, diff in _legacy_facts:
    QA_ALL.extend(_variants(q, ans, wrong, diff))

