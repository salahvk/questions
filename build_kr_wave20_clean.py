# Systematic stem expansion (safe variants only)
def _expand_stems(rows: list[tuple[str, str, list[str], str]]) -> list[tuple[str, str, list[str], str]]:
    import re as _re2
    out: list[tuple[str, str, list[str], str]] = []
    seen: set[str] = set()
    yr = _re2.compile(r"^\d{4}$")

    def _push(q: str, ans: str, wrong: list[str], diff: str) -> None:
        q = q.strip()
        if not q or q in seen or len({ans, *wrong[:3]}) < 4:
            return
        seen.add(q)
        out.append((q, ans, wrong[:3], diff))

    for q, ans, wrong, diff in rows:
        if "ആരാണ്/എതാണ്" in q:
            q = q.replace(" ആരാണ്/എതാണ്?", " ഏത്?").replace(" ആരാണ്/എതാണ്", " ഏത്")
        _push(q, ans, wrong, diff)
        if " ആരാണ്?" in q:
            _push(q.replace(" ആരാണ്?", " ഏത്?"), ans, wrong, diff)
        if " ഏത്?" in q and ("വർഷം" in q or yr.match(ans.strip())):
            _push(q.replace(" ഏത്?", " എപ്പോൾ?"), ans, wrong, diff)
        if q.endswith("?") and not q.startswith("കേരള നവോത്ഥാന"):
            _push(f"കേരള നവോത്ഥാനത്തിൽ — {q}", ans, wrong, diff)
        if q.endswith("?") and "?" not in q[:-1]:
            _push(q.replace("?", " — ഏത്?"), ans, wrong, diff)

    return out

