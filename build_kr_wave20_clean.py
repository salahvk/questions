    YEAR = re.compile(r"^\\d{4}$")


    def _pool(items: list[str], correct: str) -> list[str]:
        return [x for x in items if x != correct]


    def _typed_pool(q: str, ans: str, wrong: list[str]) -> list[str]:
        if YEAR.match(ans.strip()):
            years = [x for x in wrong + [ans] if YEAR.match(str(x).strip())]
            return years if len(set(years)) >= 4 else wrong + [ans]
        return wrong + [ans]


    def _add(out, existing, rng, q, ans, wrong, diff="medium"):
        if MIXED.search(q + ans + "".join(wrong)):
            return
        pool = list(dict.fromkeys(_typed_pool(q, ans, wrong)))
        add_candidate(out, existing, rng, q, ans, _pool(pool, ans)[:3], diff, pool)


    def _emit_pairs(
        out, existing, rng,
        rows: list[tuple[str, str]],
        fwd: list[str],
        pool_b: list[str],
        diff: str = "medium",
    ) -> None:
        for a, b in rows:
            for tmpl in fwd:
                _add(out, existing, rng, tmpl.format(a=a, b=b), b, _pool(pool_b, b)[:3], diff)


    def _emit_pairs_rev(
        out, existing, rng,
        rows: list[tuple[str, str]],
        rev: list[str],
        pool_a: list[str],
        diff: str = "medium",
    ) -> None:
        for a, b in rows:
            for tmpl in rev:
                _add(out, existing, rng, tmpl.format(a=a, b=b), a, _pool(pool_a, a)[:3], diff)


