        '"""Verified Malayalam-only constitution fact rows for coi_wave20_facts.py."""\n\n',
        'from __future__ import annotations\n\n\n',
        'def ml_num(n: int) -> str:\n',
        '    m = {\n',
        '        1: "ഒന്ന്", 2: "രണ്ട്", 3: "മൂന്ന്", 4: "നാല്", 5: "അഞ്ച്", 6: "ആറ്", 7: "ഏഴ്",\n',
