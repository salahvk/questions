from refill_common import Candidate, add_candidate

Fact = tuple[str, str, list[str], str]


def _pool(items: list[str], correct: str) -> list[str]:
    return [x for x in items if x != correct]


def _emit(
    out: list[Candidate],
    existing: set[str],
    rng: random.Random,
    prefix: str,
    facts: list[Fact],
    *,
    function_entity: str | None = None,
) -> None:
    terms = list(dict.fromkeys(t for t, _, _, _ in facts))
    for term, ans, wrong, diff in facts:
        add_candidate(out, existing, rng, f"{prefix} '{term}'?", ans, wrong, diff, pool=wrong + [ans])
        add_candidate(
            out, existing, rng,
            f"'{ans}' ഏതിനെ സൂചിപ്പിക്കുന്നു?",
            term, _pool(terms, term)[:6], diff, pool=terms,
        )
        if function_entity:
            add_candidate(
                out, existing, rng,
                f"'{ans}' ഏത് {function_entity}-ന്റെ പ്രവർത്തനമാണ്?",
                term, _pool(terms, term)[:6], diff, pool=terms,
            )


def _emit_pairs(
    out: list[Candidate],
    existing: set[str],
    rng: random.Random,
    prefix: str,
    rows: list[tuple[str, str]],
    templates: list[str],
    diff: str = "medium",
) -> None:
    bs = list(dict.fromkeys(b for _, b in rows))
    for a, b in rows:
        for tmpl in templates:
            add_candidate(
                out, existing, rng,
                prefix + tmpl.format(a=a, b=b),
                b, _pool(bs, b)[:3], diff, pool=bs,
            )


def _emit_alt(
    out: list[Candidate],
    existing: set[str],
    rng: random.Random,
    prefix: str,
    facts: list[Fact],
    templates: list[str],
) -> None:
    terms = list(dict.fromkeys(t for t, _, _, _ in facts))
    for term, ans, wrong, diff in facts:
        for tmpl in templates:
            add_candidate(
                out, existing, rng,
                prefix + tmpl.format(term=term, ans=ans),
                ans, wrong, diff, pool=wrong + [ans],
            )
        add_candidate(
            out, existing, rng,
            f"'{ans}' ഏതിനെ സൂചിപ്പിക്കുന്നു?",
            term, _pool(terms, term)[:6], diff, pool=terms,
        )


def _emit_single(
    out: list[Candidate],
    existing: set[str],
    rng: random.Random,
    prefix: str,
    facts: list[Fact],
) -> None:
    for term, ans, wrong, diff in facts:
        add_candidate(
            out, existing, rng,
            prefix + f"'{term}'?",
            ans, wrong, diff, pool=wrong + [ans],
        )


