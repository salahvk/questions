def main() -> None:
    rng = random.Random(42)
    path = BASE / FILENAME
    data = json.loads(path.read_text(encoding="utf-8"))
    kept = list(data.get("questions", []))
    existing = load_global_stems(exclude_file=FILENAME)
    existing.update(q.get("question", "").strip() for q in kept if q.get("question"))
    start = max_id_num(kept, PREFIX) + 1

    candidates = build_facts()
    rng.shuffle(candidates)
    added = 0
    for q, opts, ans, diff in candidates:
        if added >= ADD:
            break
        if q in existing or is_filler_text(q) or option_type_mismatch(q, opts, ans):
            continue
        shuffled = list(opts)
        rng.shuffle(shuffled)
        kept.append({"id": f"{PREFIX}{start + added:03d}", "question": q, "options": shuffled, "answer": ans, "difficulty": diff})
        existing.add(q)
        added += 1

    if added < ADD:
        sys.exit(f"Only {added} facts (need {ADD}) — add more to build_facts()")

    kept = spread_consecutive_templates(kept, rng, max_run=2)
    for i, q in enumerate(kept, start=1):
        q["id"] = f"{PREFIX}{i:03d}"

    data["questions"] = kept
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"{FILENAME}: {len(kept)} (+{added})")
    subprocess.run([sys.executable, "validate_questions.py", FILENAME], cwd=BASE, check=True)


