def validate(row, name: str) -> None:
    s = ALLOWED.sub("", "".join(map(str, row)))
    m = LATIN4.search(s)
    if m:
        raise SystemExit(f"LEAK {name} {m.group()!r} in {row!r}")

def emit(name: str, hint: str, rows: list) -> str:
    for r in rows:
        validate(r, name)
    lines = [f"{name}: list[{hint}] = ["]
    lines += [f"    {r!r}," for r in rows]
    lines.append("]")
    return "\n".join(lines)

