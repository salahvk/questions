MIXED = re.compile(r"[\u0D00-\u0D7F][a-zA-Z]|[a-zA-Z][\u0D00-\u0D7F]")


def ok_pair(a: str, b: str) -> bool:
    return not MIXED.search(a + b)


def ok_triple(a: str, b: str, c: str) -> bool:
    return not MIXED.search(a + b + c)


def dedupe_pairs(rows: list[tuple[str, str]]) -> list[tuple[str, str]]:
    out: list[tuple[str, str]] = []
    seen: set[tuple[str, str]] = set()
    for a, b in rows:
        if not ok_pair(a, b):
            continue
        t = (a.strip(), b.strip())
        if t in seen:
            continue
        seen.add(t)
        out.append(t)
    return out


def dedupe_triples(rows: list[tuple[str, str, str]]) -> list[tuple[str, str, str]]:
    out: list[tuple[str, str, str]] = []
    seen: set[tuple[str, str, str]] = set()
    for a, b, c in rows:
        if not ok_triple(a, b, c):
            continue
        t = (a.strip(), b.strip(), c.strip())
        if t in seen:
            continue
        seen.add(t)
        out.append(t)
    return out


