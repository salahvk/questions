MIXED = re.compile(r"[\u0D00-\u0D7F][a-zA-Z]|[a-zA-Z][\u0D00-\u0D7F]")


def clean(rows: list[tuple[str, str]]) -> list[tuple[str, str]]:
    out: list[tuple[str, str]] = []
    seen: set[tuple[str, str]] = set()
    for a, b in rows:
        if MIXED.search(a + b):
            continue
        t = (a.strip(), b.strip())
        if t in seen:
            continue
        seen.add(t)
        out.append(t)
    return out


def clean3(rows: list[tuple[str, str, str]]) -> list[tuple[str, str, str]]:
    out: list[tuple[str, str, str]] = []
    seen: set[tuple[str, str, str]] = set()
    for a, b, c in rows:
        if MIXED.search(a + b + c):
            continue
        t = (a.strip(), b.strip(), c.strip())
        if t in seen:
            continue
        seen.add(t)
        out.append(t)
    return out


