def main() -> None:
    random.seed(42)
    path = BASE / "astronomy.json"
    global_existing = load_global_existing()

    # Start from original git HEAD quality questions
    kept: list[dict] = []
    kept_stems: set[str] = set()
    for q in git_head_questions():
        stem = q.get("question", "").strip()
        if stem and not is_filler(stem) and stem not in kept_stems:
            kept.append(q)
            kept_stems.add(stem)

    # Also keep any non-filler from current file not in git (if git missing)
    if len(kept) < 100:
        data = json.loads(path.read_text(encoding="utf-8"))
        for q in data.get("questions", []):
            stem = q.get("question", "").strip()
            if stem and not is_filler(stem) and stem not in kept_stems:
                kept.append(q)
                kept_stems.add(stem)

    existing = global_existing | kept_stems
    candidates = build_unique_candidates(existing)

    questions = list(kept)
    for q, opts, ans, diff in candidates:
        if len(questions) >= TARGET:
            break
        if q in kept_stems:
            continue
        questions.append(make_entry(len(questions) + 1, q, opts, ans, diff))
        kept_stems.add(q)

    # If still under target, fail loudly — do not add filler
    if len(questions) < TARGET:
        raise SystemExit(
            f"Only {len(questions)} unique questions available (need {TARGET}). "
            "Add more real facts to build_unique_candidates(); never use numbered templates."
        )

    for i, q in enumerate(questions, 1):
        q["id"] = f"{PREFIX}{i:04d}"

    path.write_text(
        json.dumps({"questions": questions[:TARGET]}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )

    filler_left = sum(1 for q in questions if is_filler(q["question"]))
    print(f"astronomy.json: {len(questions[:TARGET])} questions")
    print(f"filler patterns remaining: {filler_left}")
    print(f"from git/original kept: {len(kept)}")


if __name__ == "__main__":
    main()
