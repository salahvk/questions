def pad_rk(base: list[tuple[str, str]], n: int = 30) -> list[tuple[str, str]]:
    rows = list(base)
    i = 0
    while len(rows) < n:
        rows.append(base[i % len(base)])
        i += 1
    return rows[:n]


def pad_rf(base: list[tuple[str, str]], n: int = 30) -> list[tuple[str, str]]:
    rows = list(base)
    i = 0
    while len(rows) < n:
        a, b = base[i % len(base)]
        rows.append((a, f"{b}-{i // len(base) + 1}"))
        i += 1
    return rows[:n]


