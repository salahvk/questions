def validate(row, name: str) -> None:
    s = ALLOWED.sub("", "".join(map(str, row)))
    m = LATIN4.search(s)
    if m:
        raise SystemExit(f"LEAK {name} {m.group()!r} :: {row!r}")


def emit(name: str, hint: str, rows: list) -> str:
    for r in rows:
        validate(r, name)
    out = [f"{name}: list[{hint}] = ["]
    out += [f"    {r!r}," for r in rows]
    out.append("]")
    return "\n".join(out)


