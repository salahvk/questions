from __future__ import annotations

from typing import List, Tuple

FactTuple = Tuple[str, str, List[str], str]


def _append_pairs(
    seeds: List[FactTuple],
    pairs: List[tuple[str, str]],
    q1: str,
    q2: str,
    diff: str = "ഇടത്തരം",
) -> None:
    pool_a = [a for _, a in pairs]
    pool_b = [b for b, _ in pairs]
    for a, b in pairs:
        seeds.append((q1.format(a), b, [x for x in pool_b if x != b][:3], diff))
        seeds.append((q2.format(b), a, [x for x in pool_a if x != a][:3], diff))


def _append_facts(
    seeds: List[FactTuple],
    facts: List[tuple[str, str]],
    template: str,
    diff: str = "ലഘു",
) -> None:
    answers = [a for _, a in facts]
    for topic, answer in facts:
        seeds.append(
            (template.format(topic), answer, [x for x in answers if x != answer][:3], diff)
        )


