from __future__ import annotations

import re

MIXED = re.compile(r"[\u0D00-\u0D7F][a-zA-Z]|[a-zA-Z][\u0D00-\u0D7F]")
_ALLOWED_LATIN = re.compile(r"\bGI\b")


def _clean(cell: str) -> str:
    return _ALLOWED_LATIN.sub("", cell)


def _check_pairs(rows: list[tuple[str, str]], label: str) -> list[tuple[str, str]]:
    for a, b in rows:
        for cell in (a, b):
            if MIXED.search(_clean(cell)):
                raise ValueError(f"{label}: mixed script in {cell!r}")
            if re.search(r"[a-zA-Z]{3,}", _clean(cell)):
                raise ValueError(f"{label}: Latin leak in {cell!r}")
    return rows


def _check_triples(rows: list[tuple[str, str, str]], label: str) -> list[tuple[str, str, str]]:
    for row in rows:
        for cell in row:
            if MIXED.search(_clean(cell)):
                raise ValueError(f"{label}: mixed script in {cell!r}")
            if re.search(r"[a-zA-Z]{3,}", _clean(cell)):
                raise ValueError(f"{label}: Latin leak in {cell!r}")
    return rows


def _facts(
    rows: list[tuple[str, str]],
    *,
    diff: str = "medium",
) -> list[tuple[str, str, list[str], str]]:
    pool = [b for _, b in rows]
    out: list[tuple[str, str, list[str], str]] = []
    for term, ans in rows:
        wrong = [x for x in pool if x != ans][:3]
        while len(wrong) < 3:
            wrong.append("മറ്റൊന്ന്")
        out.append((term, ans, wrong, diff))
    return out


